package com.dto;

public class Student {
	private int RollNo;
	private String Name;
	private int STD;
	private String DIV;
	
	public int getRollNo() {
		return RollNo;
	}
	public void setRollNo(int rollNo) {
		RollNo = rollNo;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public int getSTD() {
		return STD;
	}
	public void setSTD(int sTD) {
		STD = sTD;
	}
	public String getDIV() {
		return DIV;
	}
	public void setDIV(String dIV) {
		DIV = dIV;
	}
}
